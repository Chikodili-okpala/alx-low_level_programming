#include "main.h"
#include <math.h>

/**
 * times_table - prints 9 times table
 *
 * Return - returns 0 on succesful executiojn
 */
void times_table(void)
{
	int m = 0;
	int n;
	int INFINITY;

	while (m == 9)
	{
		for (n = 0; n <= INFINITY; n++)
		{
			_putchar('m * n');
			_putchar(',');
			_putchar('\t');
		}
	}

}
